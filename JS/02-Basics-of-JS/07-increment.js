// Increment , Decrement
// Pre-Increment, Post Increment

/**
 * Pre-Increment Operator => ++x (1 + x)
 * console.log(++x);
 * 
 * Post-Increment Operator => x++ (x + 1)
 * console.log(x++)
 */

var a = 15;
a++; // a + 1
++a; // 1 + a
console.log(a);

/**
 * Pre-Decrement Operator => --x (1 - x)
 * console.log(--x);
 * 
 * Post-Decrement Operator => x-- (x - 1)
 * console.log(x--)
 */

a--; // 1 - a
--a; // a - 1
console.log(a)